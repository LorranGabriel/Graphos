    private void addCityButtonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addCityButtonMouseClicked
        String cityOrigin = cityOriginNameField.getText().toLowerCase().trim();
        String cityDestiny = cityDestinyNameField.getText().toLowerCase().trim();

        if (cityOrigin != null && !cityOrigin.isEmpty() && cityDestiny != null && !cityDestiny.isEmpty()) {
            try {
                grapho.calcularCaminhoMinimo(cityOrigin, cityDestiny,routeField);

  
            } catch (IllegalArgumentException e) {
                feedBackField.setText(e.getMessage());
            }
        } else {
            feedBackField.setText("Nome da cidade de origem ou destino inv√°lido.");
        }   
    }//GEN-LAST:event_addCityButtonMouseClicked

